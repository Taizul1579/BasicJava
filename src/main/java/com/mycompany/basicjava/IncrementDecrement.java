
package com.mycompany.basicjava;

public class IncrementDecrement {
    public static void main(String[] args) {
        int x,y;
        x = 3;
        y = ++x;
        x = --x;
        
        System.out.println("Increment: "+y);
        System.out.print("Decrement: "+x);
    }
    
}
